package com.truphone.lpa.modem.impl;

public class RilException extends Exception {

	public RilException() {
	}

	public RilException(final String message) {
		super(message);
	}

	public RilException(final Throwable cause) {
		super(cause);
	}

	public RilException(final String message, final Throwable cause) {
		super(message, cause);
	}

}
